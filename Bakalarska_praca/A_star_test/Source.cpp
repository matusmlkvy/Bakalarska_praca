// A Utility Function to check whether given cell (row, col)
// is a valid cell or not.
template <size_t ROW, size_t COL>
bool isValid(const array<array<int, COL>, ROW>& grid, const Pair& point)
{ // Returns true if row number and column number is in
  // range
    if (ROW > 0 && COL > 0)
        return (point.first >= 0) && (point.first < ROW) && (point.second >= 0) && (point.second < COL);

    return false;
}

// A Utility Function to check whether the given cell is
// blocked or not
template <size_t ROW, size_t COL>
bool isUnBlocked(const array<array<int, COL>, ROW>& grid, const Pair& point)
{
    // Returns true if the cell is not blocked else false
    return isValid(grid, point) && grid[point.first][point.second] == 1;
}



//draw map with obstacles, with grid and destination point
template <size_t ROW, size_t COL>
void drawmap(const array<array<int, COL>, ROW>& grid, int _pixels, MyRobotList& robots)
{
    
    int R = (int)(0.035 * TICKS_PER_METER / TICKS_PER_PIXEL);
    int pixels = _pixels;

    Mat map = Mat::zeros(ROW * pixels, COL * pixels, CV_8UC3);
    //draw map
    for (int ky = 0; ky < ROW; ky++)
    {
        for (int kx = 0; kx < COL; kx++)
        {
            Pair occupation(ky, kx);
            if (!isUnBlocked(grid, occupation))
            {
                rectangle(map, Point(kx * pixels, ky * pixels + pixels), Point(kx * pixels + pixels, ky * pixels), Scalar(0, 0, 255), FILLED);
            }
            else if (isUnBlocked(grid, occupation))
            {
                rectangle(map, Point(kx * pixels, ky * pixels + pixels), Point(kx * pixels + pixels, ky * pixels), Scalar(0, 0, 0), FILLED);
            }
        }
    }
    //draw grid
    for (int kx = 0; kx < COL; kx++)
    {
        int x = kx * pixels;
        line(map, Point(x, 0), Point(x, ROW * pixels), Scalar(255, 255, 255), 1);
    };
    for (int ky = 0; ky < ROW; ky++)
    {
        int y = ky * pixels;
        line(map, Point(0, y), Point(COL * pixels, y), Scalar(255, 255, 255), 1);
    };


    

    //robot
    for (int i = 0; i < robots.size(); i++)
    {
        Position_t pos = robots[i]->position();
        circle(map, Point(pos.x / TICKS_PER_PIXEL, pos.y / TICKS_PER_PIXEL), R, Scalar(255, 0, 0), FILLED);
        //draw course to a robot
        float degree = (float)pos.psi * 0.01 * M_PI / 180.0;
        int x = pos.x / TICKS_PER_PIXEL + R * cos(degree);
        int y = pos.y / TICKS_PER_PIXEL + R * sin(degree);
        line(map, Point(pos.x / TICKS_PER_PIXEL, pos.y / TICKS_PER_PIXEL), Point(x, y), Scalar(0, 255, 255), 2);
    }
    imshow("Povodna mapa", map);
    waitKey(10);
}


